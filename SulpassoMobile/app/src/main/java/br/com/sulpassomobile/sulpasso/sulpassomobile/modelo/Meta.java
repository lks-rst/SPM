public class Meta
{
    private String familia;
    private int meta_c;
    private int realizado_c;
    private float meta_v;
    private float realizado_v;
    private float meta_co;
    private float realizado_co;
    private float meta_f;
    private float realizado_f;

    public String getFamilia() { return familia; }

    public void setFamilia(String familia) { this.familia = familia; }

    public int getMeta_c() { return meta_c; }

    public void setMeta_c(int meta_c) { this.meta_c = meta_c; }

    public int getRealizado_c() { return realizado_c; }

    public void setRealizado_c(int realizado_c) { this.realizado_c = realizado_c; }

    public float getMeta_v() { return meta_v; }

    public void setMeta_v(float meta_v) { this.meta_v = meta_v; }

    public float getRealizado_v() { return realizado_v; }

    public void setRealizado_v(float realizado_v) { this.realizado_v = realizado_v; }

    public float getMeta_co() { return meta_co; }

    public void setMeta_co(float meta_co) { this.meta_co = meta_co; }

    public float getRealizado_co() { return realizado_co; }

    public void setRealizado_co(float realizado_co) { this.realizado_co = realizado_co; }

    public float getMeta_f() { return meta_f; }

    public void setMeta_f(float meta_f) { this.meta_f = meta_f; }

    public float getRealizado_f() { return realizado_f; }

    public void setRealizado_f(float realizado_f) { this.realizado_f = realizado_f; }
}